import numpy as np

def announce(func):
    title_len =            len('######################################')
    l = title_len - len(func)
    title = round(l/2)*' ' + func + (l - round(l/2))*' '
    print('--------------------------------------------------------------------------------')
    print('                    ########################################                    ')
    print('------------------- #' + title +                          '# -------------------')
    print('                    ########################################                    ')
    print('--------------------------------------------------------------------------------')

def succ_fail(succ,fail):
    print('#######################################')
    print('#                                     #')
    print('# Successes:       %d' % (succ) + ' '*int(len('                  ') - np.floor(abs(np.log10(succ + 0.5)))) + '#')
    print('#                                     #')
    print('# Failures:        %d'% (fail) + ' '*int(len('                  ') - np.floor(abs(np.log10(fail + 0.5)))) + '#')
    print('#                                     #')
    print('#######################################')
